%每行代表一个产品，每列代表一道工序
%设备矩阵
% A = [3,1,2,3,4,0,0,0;
%     1,4,2,3,0,0,0,0;
%     3,4,1,2,1,0,0,0;
%     2,3,4,1,4,3,0,0;
%     4,2,3,4,1,3,4,0;
%     1,2,1,3,4,1,3,1];
%时间矩阵
% B = [8,2,4,24,6,0,0,0;
%     4,5,3,4,0,0,0,0;
%     3,7,15,20,8,0,0,0;
%     7,6,21,1,16,3,0,0;
%     10,4,8,4,12,6,1,0;
%     1,4,7,3,5,2,5,8];
function l = init(A)

%生成基础序列
C = (A~=0);
list = zeros(1,sum(C(:)));
w = size(A,1);
b = 1;
for i=1:w
    n = sum(A(i,:)~=0);
    e = b+n-1;
    
    for j = b:e
        list(j) = i;
    end
    b = e + 1;
end
l = list;